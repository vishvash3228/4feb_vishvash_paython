#include <iostream>
using namespace std;

main() {
    int a = 10, b = 5;
    float x = 3.5, y = 2.5;
    const double PI = 3.14159;
    char grade = 'A';
    string name = "John";

    cout << "Sum of integers: " << a + b << endl;
    cout << "Product of floats: " << x * y << endl;
    cout << "Value of constant PI: " << PI << endl;
    cout << "Character grade: " << grade << endl;
    cout << "Name: " << name << endl;
}

